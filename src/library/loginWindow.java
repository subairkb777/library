    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String userName,passWord;
        userName=jTextField1.getText();
        passWord=jPasswordField1.getText();
        
        Library lib = new Library();
        try
        {
            if(userName.isEmpty() || passWord.isEmpty())
        {
            erroMessageBox.setText("user name or password is null");
            reset(jTextField1);
            reset(jPasswordField1);
            
        }
            else if(lib.libraryLogin(userName, passWord))
        {
            mainWindow   mainwd= new mainWindow();
            //lib.close();
            this.dispose();
            mainwd.setVisible(true);
        }
        else
        {
            erroMessageBox.setText("user name or password is incorrect");
             reset(jTextField1);
            reset(jPasswordField1);
            
        }
        
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, "error");
            lib.close();
            this.dispose();
            
        }
      
        
            
    }//GEN-LAST:event_jButton1ActionPerformed

